                                            <li>Personalizing cards with messages and embellishments.</li>
                                        </ul>
                                        <p><b>Week 4: Introduction to Origami</b></p>
                                        <ul>
                                            <li>Basics of origami: history and fundamental folds.</li>
                                            <li>Simple origami projects: cranes, flowers, and boxes.</li>
                                            <li>Tips for precise folding and finishing.</li>
                                        </ul>
                                        <h3>Month 2: Advanced Techniques and Projects</h3>
                                        <p><b>Week 5: Paper Quilling</b></p>
                                        <ul>
                                            <li>Introduction to quilling tools and materials.</li>
                                            <li>Basic quilling shapes: coils, teardrops, and scrolls.</li>
                                            <li>Creating quilled designs and incorporating them into projects.</li>
                                        </ul>
                                        <p><b>Week 6: 3D Paper Sculptures</b></p>
                                        <ul>
                                            <li>Techniques for building 3D structures with paper.</li>
                                            <li>Creating simple 3D models: animals, geometric shapes, and decorations.</li>
                                            <li>Structural integrity and finishing touches.</li>
                                        </ul>
                                        <p><b>Week 7: Scrapbooking and Paper Albums</b></p>
                                        <ul>
                                            <li>Basics of scrapbooking: themes, layouts, and design elements.</li>
                                            <li>Techniques for creating decorative pages and albums.</li>
                                            <li>Adding photographs, memorabilia, and journaling to your scrapbook.</li>
                                        </ul>
                                        <p><b>Week 8: Final Project and Showcase</b></p>
                                        <ul>
                                            <li>Planning and executing a comprehensive paper craft project.</li>
