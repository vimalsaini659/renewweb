                                            <li>Creating simple paper shapes and models.</li>
                                        </ul>
                                        <p><b>Week 3: Creating Cards and Envelopes
                                            </b></p>
                                        <ul>
                                            <li>Techniques for making greeting cards and envelopes.</li>
